package com.training.vo;

public class ProductData {
	
	private String name;
	private String brand;
	private int Id;
	String getName() {
		return name;
	}
	void setName(String name) {
		this.name = name;
	}
	String getBrand() {
		return brand;
	}
	void setBrand(String brand) {
		this.brand = brand;
	}
	int getId() {
		return Id;
	}
	void setId(int id) {
		Id = id;
	}
	
	

}
